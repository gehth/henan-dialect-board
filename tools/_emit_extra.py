def to_pinyin(word: str) -> str:
    if word in PINYIN_OVERRIDE:
        return PINYIN_OVERRIDE[word]
    if pinyin is None:
        return ""
    try:
        parts = pinyin(word, style=Style.TONE, heteronym=False)
        return " ".join(p[0] for p in parts)
    except Exception:
        return ""


