def auto_pinyin(text):
    parts = pinyin(text, style=Style.TONE, heteronym=False, errors="default")
    return " ".join(p[0] for p in parts)


def build_pinyin(dialect):
    if dialect in OVERRIDE:
        return OVERRIDE[dialect]
    py = auto_pinyin(dialect)
    # 若自动注音含生僻未识别（出现原字），回退到逐字 override 或标记
    if any(ch in py for ch in dialect if not "\u4e00" <= ch <= "\u9fff" and ch not in "儿"):
        pass
    return py


