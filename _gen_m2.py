combined = list(PHRASES) + list(SUPPLEMENT)
out = []
warns = []
seen = set()
skipped = 0
for d, m, cat, expl in combined:
    if d in existing or d in seen:
        skipped += 1
        continue
    seen.add(d)
    py = build_pinyin(d)
    leftover = [ch for ch in d if ch not in "儿" and ch in py]
    if leftover and d not in OVERRIDE:
        warns.append((d, py))
    out.append({"dialect": d, "mandarin": m, "pinyin": py,
                "explanation": expl, "category": cat})

